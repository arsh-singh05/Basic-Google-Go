package main
import "fmt"

func main() 
{
    var a [5]int
    a[0] = 1
    a[1] = 2
    a[2] = 3
    a[3] = 4
    a[4] = 5
    fmt.Println(a)
}    

// This program declares an array `a` of length 5 and assigns values to each element. 
// Then it uses the `fmt` package to print the entire array to the console.
